public class Pet {

    //Attributes
    private String name;

    //Constructors
    public Pet() {
    }

    public Pet(String name) {
        this.name = name;
    }

    //Methods
    //Getters and Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
